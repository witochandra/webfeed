import 'package:xml/xml.dart';

class PeerLink {
  final String? type;
  final String? href;
  final String? value;

  PeerLink({
    this.type,
    this.href,
    this.value,
  });

  factory PeerLink.parse(XmlElement element) {
    return PeerLink(
      type: element.getAttribute('type'),
      href: element.getAttribute('href'),
      value: element.text,
    );
  }
}
